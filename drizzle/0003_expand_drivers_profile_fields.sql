  ADD COLUMN IF NOT EXISTS podiums INTEGER NOT NULL DEFAULT 0,
  ADD COLUMN IF NOT EXISTS poles INTEGER NOT NULL DEFAULT 0,
  ADD COLUMN IF NOT EXISTS championships INTEGER NOT NULL DEFAULT 0,
  ADD COLUMN IF NOT EXISTS dob TEXT,
  ADD COLUMN IF NOT EXISTS pob TEXT,
  ADD COLUMN IF NOT EXISTS gp_entered INTEGER NOT NULL DEFAULT 0,
  ADD COLUMN IF NOT EXISTS career_points TEXT NOT NULL DEFAULT '0',
  ADD COLUMN IF NOT EXISTS best_finish TEXT NOT NULL DEFAULT '—',
