  ADD COLUMN IF NOT EXISTS dnfs INTEGER NOT NULL DEFAULT 0;

UPDATE drivers
SET
  short_name = COALESCE(short_name, code),
  team_color = COALESCE(team_color, '#999999'),
  nationality = COALESCE(nationality, country),
  flag = COALESCE(flag, '🏁'),
  dob = COALESCE(dob, '01/01/1900'),
  pob = COALESCE(pob, 'Unknown');

